import { Seeder } from 'typeorm-extension';
import { DataSource } from 'typeorm';
import { ThemeEntity } from '@theme/entities';

export default class ThemeSeeder implements Seeder {
  async run(
    dataSource: DataSource,
  ) {
    console.info('ThemeSeeder Run');
    const repository = dataSource.getRepository(ThemeEntity);

    const themeEntities = THEMES.map((theme) => ThemeEntity.of({ text: theme }));

    await repository.createQueryBuilder()
      .insert()
      .values(themeEntities)
      .orIgnore(true)
      .execute();
  }
}

